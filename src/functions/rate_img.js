export default function rate_to_img(score) {
    const img_name = 0
    if (score < 34) {
        const img_name = '0.PNG'
    }
    if (score < 67) {
        const img_name = '0.PNG'
    } 
    if (score < 100) {
        const img_name = '0.PNG'
    } 
    if (score < 150) {
        const img_name = '0.PNG'
    } 
    if (score < 200) {
        const img_name = '0.PNG'
    }
    else {
        const img_name = '0.PNG'
    }
    return img_name

}


